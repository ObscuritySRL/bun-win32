const _filetimePackBuffer = Buffer.alloc(8);

/**
 * Pack `dwLowDateTime` + `dwHighDateTime` into a `PACKED_FILETIME` for the
 * legacy URL-cache APIs that take `FILETIME` by value (`CommitUrlCacheEntryA`,
 * `SetUrlCacheEntryInfoW`, etc.). Pass the resulting `bigint` directly.
 */
export function packFILETIME(dwLowDateTime: number, dwHighDateTime: number): PACKED_FILETIME {
  _filetimePackBuffer.writeUInt32LE(dwLowDateTime, 0);
  _filetimePackBuffer.writeUInt32LE(dwHighDateTime, 4);
  return _filetimePackBuffer.readBigUInt64LE(0);
}
