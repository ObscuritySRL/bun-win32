/**
 * A `LUID` packed into a 64-bit integer for by-value passing in the x64 ABI.
 *
 * On x64, `LUID` (8 bytes: `DWORD LowPart` + `LONG HighPart`) is passed in a
 * single register. Use `packLUID(lowPart, highPart)` to construct one.
 */
export type LUID = bigint;

const _luidPackBuf = Buffer.alloc(8);

/** Pack a `LUID` (low/high 32-bit parts) into a single 64-bit value for by-value passing. */
export function packLUID(lowPart: number, highPart: number): LUID {
  _luidPackBuf.writeUInt32LE(lowPart, 0);
  _luidPackBuf.writeInt32LE(highPart, 4);
  return _luidPackBuf.readBigUInt64LE(0);
}
