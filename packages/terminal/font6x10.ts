export const CHAR_FONT_WIDTH = 6;
export const CHAR_FONT_HEIGHT = 10;

/** Code point → packed `CHAR_FONT_WIDTH × CHAR_FONT_HEIGHT` bitmap (1 = lit). */
export const charFont = new Map<number, Uint8Array>();
for (const [character, rows] of Object.entries(fontRows)) {
  const bitmap = new Uint8Array(CHAR_FONT_WIDTH * CHAR_FONT_HEIGHT);
  for (let row = 0; row < CHAR_FONT_HEIGHT; row++) {
    const rowText = rows[row] ?? '';
    for (let column = 0; column < CHAR_FONT_WIDTH; column++) {
      bitmap[row * CHAR_FONT_WIDTH + column] = rowText[column] === '#' ? 1 : 0;
    }
  }
  charFont.set(character.codePointAt(0)!, bitmap);
}
// Lowercase falls back to uppercase for any letter without its own form.
for (let codePoint = 0x61; codePoint <= 0x7a; codePoint++) {
  if (!charFont.has(codePoint)) {
    const uppercaseBitmap = charFont.get(codePoint - 0x20);
    if (uppercaseBitmap) charFont.set(codePoint, uppercaseBitmap);
  }
}
